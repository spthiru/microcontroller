void print_name(char digit[8],int sec)
{
	for(int i=0;i<120*sec;i++)
	{
		for(int j=0;j<8;j++)
		{
			PORTD=0x01<<j;
			PORTC=digit[j];
			__delay_ms(1);
			PORTC=0;
		}
	}
}